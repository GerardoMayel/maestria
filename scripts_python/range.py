# Enumeración del 10 al 100 con incrementos de 1
print(list(range(10, 101, 1)))

# Enumeración del 10 al 100 con incrementos de 5
print(list(range(10, 101, 5)))

# Enumeración de -10 a 0 con incrementos de 1
print(list(range(-10, 1, 1)))

# Enumeración de -100 a 0 con incrementos de 10
print(list(range(-100, 1, 10)))

# Enumeración del 100 al 0 con decrementos de 1
print(list(range(100, -1, -1)))

# Enumeración del 100 al 0 con decrementos de 10
print(list(range(100, -1, -10)))

# Enumeración del 100 a -100 con decrementos de 20
print(list(range(100, -101, -20)))

# Longitud de una enumeración de 100 elementos
